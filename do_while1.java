// Java Program to display numbers from 1 to 5[20]

import java.util.*;

// Program to find the sum of natural numbers from 1 to 100.

class do_while1
 {
  public static void main(String[] args) 
  {
	int i = 1;

    // do...while loop from 1 to 5
    do
	{
      System.out.println(i);
      i++;
    } while(i <= 5);
  }
}